    static int rowSize, colSize;
    static int answer = 0;
    static char[][] map;

    public static void dfs(int x, int y, int cnt){
        if(cnt == 2){
            return;
        }
        if(x == rowSize){
            answer++;
            return;
        }
        if(map[x][y] == '#'){
            dfs(x + 1, y, cnt);
        } else if(map[x][y] == '<'){
            dfs(x, y - 1, cnt);
        } else if(map[x][y] == '>'){
            dfs(x, y + 1, cnt);
        } else {
            dfs(x + 1, y, cnt + 1);
        }
    }

