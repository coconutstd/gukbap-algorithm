    public static int solution(String[] drum) {
        rowSize = drum.length;
        colSize = drum[0].length();
        map = new char[rowSize][colSize];
        int index = 0;
        for(String str : drum){
            map[index++] = str.toCharArray();
        }

        for(int i = 0; i < colSize; ++i){
            dfs(0, i, 0);
        }
        return answer;
    }

